#!/usr/bin/env python3
"""Generate TanyelOS GTK4 + GNOME Shell theme variants.

Usage (run from repo root inside Ubuntu VM):
    python3 build/scripts/build-theme.py

Requires tokens to be generated first:
    python3 build/scripts/extract-tokens.py

Outputs:
    build/themes/tanyelos-<accent>-<dark|light>/
        gtk-4.0/gtk.css
        gnome-shell/gnome-shell.css
        gnome-shell/gnome-shell-theme.gresource.xml
        index.theme
"""

import json
import os

ACCENTS = ["teal", "amber", "rose", "violet", "lime"]
VARIANTS = ["dark", "light"]


