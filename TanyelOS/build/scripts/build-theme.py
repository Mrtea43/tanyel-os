def index_theme(accent_name, dark):
    variant = "Dark" if dark else "Light"
    name = f"TanyelOS-{accent_name.capitalize()}-{variant}"
    return f"""[X-GNOME-Metatheme]
Name={name}
Comment=TanyelOS {accent_name} {variant.lower()} theme
Encoding=UTF-8
Type=X-GNOME-Metatheme
Comment=TanyelOS official theme

[Desktop Entry]
Type=X-GNOME-Metatheme
Name={name}
Comment=TanyelOS {accent_name} {variant.lower()} theme
Encoding=UTF-8

[X-GNOME-Metatheme]
GtkTheme={name}
MetacityTheme={name}
IconTheme=Adwaita
CursorTheme=Adwaita
ButtonLayout=close,minimize,maximize:
"""


def gresource_xml():
    return """<?xml version="1.0" encoding="UTF-8"?>
<gresources>
  <gresource prefix="/org/gnome/shell/theme">
    <file>gnome-shell.css</file>
  </gresource>
</gresources>
"""


def main():
    with open("build/tokens/tokens-light.json") as f:
        light_tokens = json.load(f)
    with open("build/tokens/tokens-dark.json") as f:
        dark_tokens = json.load(f)

    for accent in ACCENTS:
        for variant in VARIANTS:
            tokens = dark_tokens[accent] if variant == "dark" else light_tokens[accent]
            dir_name = f"build/themes/tanyelos-{accent}-{variant}"
            gtk_dir = f"{dir_name}/gtk-4.0"
            shell_dir = f"{dir_name}/gnome-shell"

            os.makedirs(gtk_dir, exist_ok=True)
            os.makedirs(shell_dir, exist_ok=True)

            with open(f"{gtk_dir}/gtk.css", "w") as f:
                f.write(gtk4_css(tokens))

            with open(f"{shell_dir}/gnome-shell.css", "w") as f:
                f.write(gnome_shell_css(tokens))

            with open(f"{shell_dir}/gnome-shell-theme.gresource.xml", "w") as f:
                f.write(gresource_xml())

            with open(f"{dir_name}/index.theme", "w") as f:
                f.write(index_theme(accent, variant == "dark"))

            print(f"  tanyelos-{accent}-{variant}")

    print(f"\nThemes written to build/themes/")
    print(f"Install with: sudo cp -r build/themes/tanyelos-* /usr/share/themes/")


if __name__ == "__main__":
    main()
